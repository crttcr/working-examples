## Define variables for escape sequences to set terminal colors.
##

export COLOR_BLUE=$'\033[0;34m'
export COLOR_GOLD=$'\033[1;33m'
export COLOR_GOLD_BOLD=$'\033[0;33m'
export COLOR_BLUE_BOLD=$'\033[1;34m'
export COLOR_GREEN=$'\033[0;32m'
export COLOR_GREEN_BOLD=$'\033[1;32m'
export COLOR_CYAN=$'\033[0;36m'
export COLOR_CYAN_BOLD=$'\033[1;36m'
export COLOR_PURPLE=$'\033[0;35m'
export COLOR_PURPLE_BOLD=$'\033[1;35m'
export COLOR_RED=$'\033[0;31m'
export COLOR_RED_BOLD=$'\033[1;31m'
export COLOR_GRAY=$'\033[0;37m'
export COLOR_GRAY_BOLD=$'\033[1;37m'
export COLOR_BLACK=$'\033[0;30m'
export COLOR_GREY=$'\033[1;30m'

export COLOR_FAIL=$COLOR_RED_BOLD
export COLOR_RESET=$'\033[0m'
export COLOR_STOP=$COLOR_RESET
